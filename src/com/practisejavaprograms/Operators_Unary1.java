package com.practisejavaprograms;

public class Operators_Unary1 {
	public static void main(final String[] args) {
        int x = 10;
        int y = 20;
        System.out.println(x);
        System.out.println(++x);
        System.out.println(x++);
        System.out.println(x);
        System.out.println("-------*------");
        System.out.println(y);
        System.out.println(++y);
        System.out.println(y++);
        System.out.println(y);
        System.out.println("-------*-------");
        System.out.println(--x);
        System.out.println(x--);
        System.out.println(x);
        System.out.println("-------*-------");
        System.out.println(--y);
        System.out.println(y);
        System.out.println(y--);
        System.out.println(y);
    }
}
