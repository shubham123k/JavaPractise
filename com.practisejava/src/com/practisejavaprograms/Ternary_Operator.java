package com.practisejavaprograms;

public class Ternary_Operator {
	public static void main(final String[] args) {
        final int a = 12;
        final int b = 23;
        final int max = (a > b) ? a : b;
        System.out.print(max);
        System.out.print("  ");
        short e = 25;
        final short d = 15;
        e += d;
        System.out.print(e);
    }
}
