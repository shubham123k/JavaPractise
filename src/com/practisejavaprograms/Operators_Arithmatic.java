package com.practisejavaprograms;

public class Operators_Arithmatic {
	public static void main(final String[] args) {
        final int x = 11;
        final int y = 9;
        final int z = 3;
        System.out.println(x * y / 3);
        System.out.println(x / y * z);
        System.out.println(x % y - z);
        System.out.println(x + y * z);
        System.out.println(x - y + z);
    }
}
