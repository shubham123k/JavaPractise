package com.practisejavaprograms;

public class While_loop1 {
	public static void main(final String[] args) {
        for (int i = 10; i <= 10; ++i) {
            System.out.println("Jai Maharashtra");
            for (int j = 0; j <= 11; ++j) {
                System.out.println("Avengers");
            }
        }
        System.out.println("***Jai Hind***");
    }
}
